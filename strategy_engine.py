    def generate_niche_map(self, seed_intent: str) -> Dict:
        """
        Maps out a Niche Map based on the seed intent.
        """
        seeds = [s.strip().lower() for s in seed_intent.split(",")]
        
        niche_map = {
            "seed_niche": seed_intent,
            "adjacencies": set(),
            "micro_niches": set(),
            "competitors": set(),
            "intent_analysis": []
        }

        for seed in seeds:
            # Find the best match in knowledge base
            match = self.knowledge_base.get(seed)
            if not match:
                # Try partial match
                for key, value in self.knowledge_base.items():
                    if key in seed or seed in key:
                        match = value
                        break
            
            if match:
                niche_map["adjacencies"].update(match.get("adjacencies", []))
                niche_map["micro_niches"].update(match.get("micro_niches", []))
                niche_map["competitors"].update(match.get("competitors", []))

        # Basic Intent Analysis Logic
        if "instrumental" in seed_intent or "beat" in seed_intent:
            niche_map["intent_analysis"].append("Searching for production assets or background music.")
        if "clams casino" in seed_intent or "experimental" in seed_intent:
            niche_map["intent_analysis"].append("Looking for specific textures/vibes (atmospheric, dark, ethereal).")
        if not niche_map["intent_analysis"]:
            niche_map["intent_analysis"].append("General interest in the genre/style for listening or inspiration.")

        # Convert sets to sorted lists for stability
        niche_map["adjacencies"] = sorted(list(niche_map["adjacencies"]))
        niche_map["micro_niches"] = sorted(list(niche_map["micro_niches"]))
        niche_map["competitors"] = sorted(list(niche_map["competitors"]))

        return niche_map

